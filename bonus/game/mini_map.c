#include "../cub3d_bonus.h"

int	check_wall(int x, int y, t_map *map)
{
	if (x >= 0 && x < map->cols && y >= 0 && y < map->rows)
	{
		if (map->map[y][x] == '1')
			return (1);
	}
	return (0);
}
